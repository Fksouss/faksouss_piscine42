#include<stdio.h>

int	ft_sqrt(int nb)
{
	int	x;

	x = 2;
	if (nb == 1)
		return (1);
	while (x <= nb / 2 && x <= 46340)
	{
		if (x * x == nb)
			return (x);
		else
			x++;
	}
	return (0);
}
int main()
{
	printf("%d",ft_sqrt(-400));
}